import re
import html
import pandas as pd
import numpy as np
from pathlib import Path
import json

from sklearn.feature_extraction.text import TfidfVectorizer
from sklearn.neighbors import NearestNeighbors

try:
    from imblearn.over_sampling import SMOTE
    SMOTE_AVAILABLE = True
except ImportError:
    SMOTE_AVAILABLE = False

DATA_PATH = Path("Dataset.zip")
OUTPUT_CSV = Path("preprocessed_dataset.csv")
TENSOR_PATH = Path("bert_tensors.pt")
SUMMARY_PATH = Path("preprocessing_summary.json")

# ── 1. LOAD ───────────────────────────────────────────────────────────────────
print("=" * 60)
print("STEP 1 – Loading dataset")
print("=" * 60)

print(f"  Loading raw data from {DATA_PATH}")
df = pd.read_csv(DATA_PATH)
original_rows = len(df)
print(f"Raw shape          : {df.shape}")
print(f"Columns            : {df.columns.tolist()}")
print(f"Missing (text)     : {df['text'].isnull().sum()}")
print(f"Missing (title)    : {df['title'].isnull().sum()}")
print(f"Target distribution:\n{df['target'].value_counts().sort_index()}\n")

# Drop the unnamed index column if it exists
if "Unnamed: 0" in df.columns:
    df = df.drop(columns=["Unnamed: 0"])

# ── 2. MISSING VALUE HANDLING ─────────────────────────────────────────────────
print("=" * 60)
print("STEP 2 – Handling missing values")
print("=" * 60)

before_missing = len(df)

# Where text is NaN but title exists → use title as text
mask_text_null_title_ok = df["text"].isnull() & df["title"].notna()
df.loc[mask_text_null_title_ok, "text"] = df.loc[mask_text_null_title_ok, "title"]
print(f"  Rows filled from title (text was NaN)  : {mask_text_null_title_ok.sum()}")

# If both are null → drop (edge case)
both_null = df["text"].isnull() & df["title"].isnull()
df = df[~both_null]
print(f"  Rows dropped (both text & title null)  : {both_null.sum()}")
print(f"  Remaining rows                         : {len(df)}")

# ── 3. DUPLICATES ─────────────────────────────────────────────────────────────
print("\n" + "=" * 60)
print("STEP 3 – Removing duplicates")
print("=" * 60)

before_dup = len(df)
df = df.drop_duplicates(subset=["text"])
print(f"  Duplicate rows removed : {before_dup - len(df)}")
print(f"  Remaining rows         : {len(df)}")

# ── 4. COMBINE TITLE + TEXT ───────────────────────────────────────────────────
print("\n" + "=" * 60)
print("STEP 4 – Combining title + text into single input")
print("=" * 60)

# BERT classification typically benefits from a separator between title/body
# Format: "[TITLE] <title_text> [BODY] <body_text>"
# (The actual [CLS]/[SEP] tokens are added by the tokenizer later.)
df["combined_text"] = df["title"].fillna("").str.strip() + " [SEP] " + df["text"].fillna("").str.strip()
print("  Combined field 'combined_text' created (title + [SEP] + text)")

# ── 5. NLP CLEANING FUNCTION ──────────────────────────────────────────────────
print("\n" + "=" * 60)
print("STEP 5 – NLP text cleaning")
print("=" * 60)

def clean_text(text: str, lowercase: bool = True) -> str:
    """
    Clean text:
    - Decode HTML entities (&amp; → &, &#39; → ', etc.)
    - Remove URLs (http/https/www)
    - Remove Reddit markdown: **bold**, *italic*, ~~strikethrough~~, # headers
    - Remove Reddit-specific artifacts: /r/subreddit, /u/user
    - Remove excessive newlines / whitespace
    - Strip control characters
    - Optionally lowercase (use True for bert-base-uncased)
    """
    if not isinstance(text, str):
        return ""

    # Decode HTML entities
    text = html.unescape(text)

    # Remove URLs
    text = re.sub(r"http[s]?://\S+", "", text)
    text = re.sub(r"www\.\S+", "", text)

    # Remove Reddit markdown headers (# Header, ## Header)
    text = re.sub(r"^#{1,6}\s+", "", text, flags=re.MULTILINE)

    # Remove bold/italic markers (**text**, *text*, __text__, _text_)
    text = re.sub(r"\*{1,3}(.*?)\*{1,3}", r"\1", text)
    text = re.sub(r"_{1,3}(.*?)_{1,3}", r"\1", text)

    # Remove strikethrough (~~text~~)
    text = re.sub(r"~~(.*?)~~", r"\1", text)

    # Remove Reddit subreddit / user mentions
    text = re.sub(r"/r/\w+", "", text)
    text = re.sub(r"/u/\w+", "", text)
    text = re.sub(r"r/\w+", "", text)
    text = re.sub(r"u/\w+", "", text)

    # Remove markdown links [text](url) → text
    text = re.sub(r"\[([^\]]+)\]\([^\)]+\)", r"\1", text)

    # Remove horizontal rules (-----)
    text = re.sub(r"^-{3,}\s*$", "", text, flags=re.MULTILINE)

    # Remove excess newlines / whitespace (collapse 2+ newlines → 1 space)
    text = re.sub(r"\n+", " ", text)
    text = re.sub(r"\r+", " ", text)
    text = re.sub(r"\t+", " ", text)

    # Remove control characters (non-printable ASCII)
    text = re.sub(r"[\x00-\x08\x0b\x0c\x0e-\x1f\x7f]", "", text)

    # Collapse multiple spaces
    text = re.sub(r" {2,}", " ", text)

    # Strip leading/trailing whitespace
    text = text.strip()

    # Lowercase (required for bert-base-uncased)
    if lowercase:
        text = text.lower()

    return text

df["clean_text"] = df["combined_text"].apply(clean_text)

# Stats after cleaning
df["clean_len"] = df["clean_text"].apply(len)
print(f"  Cleaning complete. Character length stats:")
print(df["clean_len"].describe().to_string())

# ── 6. REMOVE NEAR-EMPTY ROWS ─────────────────────────────────────────────────
print("\n" + "=" * 60)
print("STEP 6 – Removing near-empty rows (< 10 chars after cleaning)")
print("=" * 60)

before_empty = len(df)
df = df[df["clean_len"] >= 10].reset_index(drop=True)
after_near_empty = len(df)
print(f"  Rows removed (< 10 chars) : {before_empty - after_near_empty}")
print(f"  Final row count           : {after_near_empty}")

# ── 7. BALANCING WITH SMOTE ─────────────────────────────────────────────────
print("\n" + "=" * 60)
print("STEP 7 – Balancing dataset with SMOTE")
print("=" * 60)

if not SMOTE_AVAILABLE:
    print("  [WARNING] imbalanced-learn is not installed; SMOTE balancing is skipped.")
    print("  Install it with: pip install imbalanced-learn")
else:
    # Use TF-IDF to convert text into numeric features so SMOTE can operate.
    print("  Vectorizing cleaned text for SMOTE ...")
    vectorizer = TfidfVectorizer(max_features=4096, ngram_range=(1, 2), stop_words="english")
    X = vectorizer.fit_transform(df["clean_text"].fillna(""))
    y = df["target"].values

    # Apply SMOTE to balance the classes
    smote = SMOTE(random_state=42)
    X_res, y_res = smote.fit_resample(X, y)

    if len(y_res) == len(y):
        print("  Dataset already balanced; no SMOTE samples added.")
    else:
        print(f"  SMOTE generated {len(y_res) - len(y)} synthetic samples.")

    # Map resampled vectors back to the nearest original texts to preserve realistic samples.
    print("  Mapping oversampled vectors to nearest original texts ...")
    knn = NearestNeighbors(n_neighbors=1, metric="cosine")
    knn.fit(X)
    nearest_original = knn.kneighbors(X_res, return_distance=False).flatten()

    df = df.iloc[nearest_original].reset_index(drop=True)
    df["target"] = y_res
    print(f"  Balanced target distribution:\n{df['target'].value_counts().sort_index()}\n")

# ── 8.TOKENIZATION ──────────────────────────────────────────────────────
print("\n" + "=" * 60)
print("STEP 8 – BERT Tokenization")
print("=" * 60)

try:
    from transformers import BertTokenizer
    import torch

    MODEL_NAME = "bert-base-uncased"
    MAX_LEN = 512

    print(f"  Loading tokenizer: {MODEL_NAME} ...")
    tokenizer = BertTokenizer.from_pretrained(MODEL_NAME)

    print(f"  Tokenizing {len(df)} samples with max_length={MAX_LEN} ...")

    encodings = tokenizer(
        df["clean_text"].tolist(),
        max_length=MAX_LEN,
        padding="max_length",
        truncation=True,
        return_attention_mask=True,
        return_token_type_ids=True,
        return_tensors="pt",
    )

    input_ids      = encodings["input_ids"]         # (N, 512)
    attention_mask = encodings["attention_mask"]     # (N, 512)
    token_type_ids = encodings["token_type_ids"]     # (N, 512)
    labels         = torch.tensor(df["target"].values, dtype=torch.long)

    print(f"  input_ids shape      : {input_ids.shape}")
    print(f"  attention_mask shape : {attention_mask.shape}")
    print(f"  token_type_ids shape : {token_type_ids.shape}")
    print(f"  labels shape         : {labels.shape}")

    # Check truncation
    truncated = (attention_mask.sum(dim=1) == MAX_LEN).sum().item()
    print(f"  Sequences truncated to {MAX_LEN} tokens : {truncated} ({truncated/len(df)*100:.1f}%)")

    # Save tensors
    torch.save(
        {
            "input_ids": input_ids,
            "attention_mask": attention_mask,
            "token_type_ids": token_type_ids,
            "labels": labels,
        },
        TENSOR_PATH,
    )
    print(f"\n  Saved tokenized tensors → {TENSOR_PATH}")
    tokenizer_saved = True

except Exception as e:
    print(f"  [WARNING] Tokenizer step skipped: {e}")
    print("  (Install torch + transformers to enable tokenization)")
    tokenizer_saved = False

# ── 9. SAVE CLEANED CSV ───────────────────────────────────────────────────────
print("\n" + "=" * 60)
print("STEP 9 – Saving cleaned dataset")
print("=" * 60)

output_cols = ["clean_text", "title", "text", "target"]
df_out = df[output_cols].copy()
df_out.to_csv(OUTPUT_CSV, index=False)
print(f"  Saved: {OUTPUT_CSV}  ({len(df_out)} rows)")

# ── 10. SUMMARY REPORT ─────────────────────────────────────────────────────────
print("\n" + "=" * 60)
print("PREPROCESSING SUMMARY")
print("=" * 60)

summary = {
    "original_rows": int(original_rows),
    "after_missing_fill": int(before_dup),
    "after_dedup": int(before_empty),
    "final_rows": int(len(df)),
    "rows_removed_total": int(original_rows - len(df)),
    "missing_text_filled_from_title": int(mask_text_null_title_ok.sum()),
    "duplicates_removed": int(before_dup - before_empty),
    "near_empty_removed": int(before_empty - after_near_empty),
    "target_distribution": df["target"].value_counts().sort_index().to_dict(),
    "avg_clean_text_chars": float(df["clean_len"].mean().round(1)),
    "tokenizer": "bert-base-uncased" if tokenizer_saved else "not applied (torch missing)",
    "max_seq_length": 512,
    "output_files": [str(OUTPUT_CSV)] + ([str(TENSOR_PATH)] if tokenizer_saved else []),
}

print(json.dumps(summary, indent=2))

with open(SUMMARY_PATH, "w") as f:
    json.dump(summary, f, indent=2)

print("\nDone! ✓")
